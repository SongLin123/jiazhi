<template>
  <div id="app">
    <router-view></router-view>
  </div>
</template>

<script>
export default {
  name: 'app',
  created(){
    let user=navigator.userAgent.toLowerCase();
    if (user.indexOf('android')>0||user.indexOf('iphone')>0){
      localStorage.setItem('agent','phone')
      return
    }
    localStorage.setItem('agent','pc')
  }
}
</script>

